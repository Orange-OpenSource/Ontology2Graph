'''This function merge several knowledge Graphs in an only one. You have to pass as a parameter the
 folder where are stored the Graphs generated by LLM and the number of node's occurrence you want 
 to keep in all these files'''

import sys
import os
import glob
import shutil
from pathlib import Path
from merge_ttl_files.utils.utils_merge import rename_duplicates_nodes,remove_duplicate_prefix
from merge_ttl_files.utils.utils_merge import find_duplicates_nodes,check_ttl
#from utils.utils_merge import rename_duplicates_nodes,remove_duplicate_prefix,find_duplicates_nodes
#from utils.utils_merge import check_ttl

def merge_ttl(path,nbr_occ):
    '''function to merge ttl files'''


    #arg = sys.argv[1:]
    #PATH= arg[0]
    #nbr_occ= int(arg[1])

#if nbr_occ==0:
#    print('Number of occurence nodes = 0 is not authorized as it wil rename all the nodes,')
#    sys.exit()

    #path_result=f'{PATH}/merged_graph/'
    path_result=f'{path}/merged_graph/'
    merged_file=f'{path_result}/merged_graph_{nbr_occ}.ttl'

    bad_path_result=f'{path_result}/bad_Turtle_Syntax'
    bad_merged_file=f'{bad_path_result}/merged_graph_BAD.ttl'
    output_file_temp=f'{path_result}/temp.ttl'

    path_ontology='../generate_ttl_files/ontologies/Noria.ttl'

    #manage duplicate nodes in ttl files
    duplicates=find_duplicates_nodes(path,path_ontology)
    rename_duplicates_nodes(path,duplicates,nbr_occ)

    #Merge the ttl file
    all_graphs = [f.name for f in Path(path).iterdir() if f.is_file()]
    os.makedirs(f'{path_result}',exist_ok=True)

    with open(output_file_temp, 'w', encoding='utf-8') as outfile:
        for filename in all_graphs:
            # Open each input file in read mode
            with open(f'{path}/{filename}', 'r', encoding='utf-8') as infile:
                # Read the content and write it to the output file
                content = infile.read()
                outfile.write(content)
                outfile.write('\n')  # Adds a newline between files

    # manage prefix in merged file
    remove_duplicate_prefix(output_file_temp,merged_file)

    # restore ttl file
    for file in glob.glob(os.path.join(path, '*.ttl')):
        os.remove(file)

    src_folder=Path(f'{path}/raw_file')
    dst_folder=Path(f'{path}/')

    for file in src_folder.iterdir():
        if file.is_file():
            shutil.copy2(file, dst_folder / file.name)

    # validate ttl syntax
    check_ttl(merged_file,bad_merged_file,bad_path_result,1)

    return(path_result)
